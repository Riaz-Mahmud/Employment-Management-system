    public void  updateEmpInfo()
    {
        String empid = empidtxt1.getText();
        try
        {
            Connection con = SQLConnection.getConnection();
            String sql = "UPDATE empinfo SET department=?, name=?, birthday=?, number=?, email=?, salary=?, address=?, status=? WHERE empid='"+empid+"'";
            PreparedStatement pst = con.prepareStatement(sql);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(birthdaytxt1.getDate());
            pst.setString(1, departtxt1.getSelectedItem().toString());
            pst.setString(2, nametxt1.getText());
            pst.setString(3, date);
            pst.setString(4, numbertxt1.getText());
            pst.setString(5, emailtxt1.getText());
            pst.setString(6, salarytxt1.getText());
            pst.setString(7, addresstxt1.getText());
            pst.setString(8, jLabel113.getText());

            if(pst.executeUpdate() > 0)
            {
                empidtxt1.setText("");
                departtxt1.setSelectedItem("Select");
                nametxt1.setText("");
                birthdaytxt1.setDate(null);
                numbertxt1.setText("");
                emailtxt1.setText("");
                salarytxt1.setText("");
                addresstxt1.setText("");
                jLabel113.setText("");
                JOptionPane.showMessageDialog(null, "Update Success");
                
            }
        con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
