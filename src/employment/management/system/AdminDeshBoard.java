    public void AddnewAdmin()
    {
        if(nadname.getText().trim().isEmpty() || nadnumber.getText().trim().isEmpty() || naddate.getDate()==null || nadusername.getText().trim().isEmpty() || nadpassword.getText().trim().isEmpty())
        {
            if(nadname.getText().trim().isEmpty())
            {
                nadname_lbl.setText("Empty");
            }
            if(nadnumber.getText().trim().isEmpty())
            {
                nadnumber_lbl.setText("Empty");
            }
            if(naddate.getDate()==null)
            {
                naddate_lbl.setText("Empty");
            }
            if(nadusername.getText().trim().isEmpty())
            {
                nadusername_lbl.setText("Empty");
            }
            if(nadpassword.getText().trim().isEmpty())
            {
                nadpassword_lbl.setText("Empty");
            }
        }
        else
        {
            try
            {
                Connection con = SQLConnection.getConnection();
                Statement st = con.createStatement();
                String getusername = nadusername.getText();
                String sql3 = "SELECT username FROM admininfo Where username='"+getusername+"'";
                ResultSet rs = st.executeQuery(sql3);
                
                if(rs.next())
                {
                    String abc = rs.getString(1);
                    if(abc.equals(getusername))
                    {
                        JOptionPane.showMessageDialog(null, "Username Already taken");
                    }
                }
                else
                {
                    try
                    {
                        String sql = "INSERT INTO admininfo ( Name, number, birthdate, username, password, status) VALUES (?, ?, ?, ?, ?, ?)";
                        PreparedStatement pst = con.prepareStatement(sql);
                        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                        String date = sdf.format(naddate.getDate());
                        pst.setString(1, nadname.getText());
                        pst.setString(2, nadnumber.getText());
                        pst.setString(3, date);
                        pst.setString(4, getusername);
                        pst.setString(5, nadpassword.getText());
                        pst.setString(6, "active");

                        if(pst.executeUpdate() > 0)
                        {
                            JOptionPane.showMessageDialog(null, "New Account Created- \n"
                                    + "Username: "+getusername+"\n"
                                    + "Password : "+nadpassword.getText()+" ");
                            nadname.setText("");
                            nadnumber.setText("");
                            naddate.setDate(null);
                            nadusername.setText("");
                            nadpassword.setText("");
                        }
                        con.close();
                    }
                    catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }
                con.close();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
