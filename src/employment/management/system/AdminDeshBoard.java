    public void Submit()
    {
        if(empidtxt.getText().trim().isEmpty() || departtxt.getSelectedItem().equals("Select") || nametxt.getText().trim().isEmpty() || birthdaytxt.getDate()==null|| 
                numbertxt.getText().trim().isEmpty() || emailtxt.getText().trim().isEmpty()  ||salarytxt.getText().trim().isEmpty()|| addresstxt.getText().trim().isEmpty() )
        {
            if(empidtxt.getText().trim().isEmpty())
            {
                jLabel27.setText("Empty");
            }
            if(departtxt.getSelectedItem().equals("Select"))
            {
                jLabel34.setText("Empty");
            }
            if(nametxt.getText().trim().isEmpty())
            {
                jLabel40.setText("Empty");
            }
            if(birthdaytxt.getDate()==null)
            {
                jLabel42.setText("Empty");
            }
            if(numbertxt.getText().trim().isEmpty())
            {
                jLabel54.setText("Empty");
            }
            
            if(emailtxt.getText().trim().isEmpty())
            {
                jLabel56.setText("Empty");
            }
            if(salarytxt.getText().trim().isEmpty())
            {
                jLabel58.setText("Empty");
            }
            if(addresstxt.getText().trim().isEmpty())
            {
                jLabel60.setText("Empty");
            }
        }
        else
        {
            try
            {
                Connection con = SQLConnection.getConnection();
                String sql = "INSERT INTO empinfo (empid, department, name, birthday, number, email, salary, address, status) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement pst = con.prepareStatement(sql);
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(birthdaytxt.getDate());
                pst.setString(1, empidtxt.getText());
                pst.setString(2, departtxt.getSelectedItem().toString());
                pst.setString(3, nametxt.getText());
                pst.setString(4, date);
                pst.setString(5, numbertxt.getText());
                pst.setString(6, emailtxt.getText());
                pst.setString(7, salarytxt.getText());
                pst.setString(8, addresstxt.getText());
                pst.setString(9, "active");

                if(pst.executeUpdate() > 0)
                {
                    empidtxt.setText("");
                    nametxt.setText("");
                    numbertxt.setText("");
                    departtxt.setSelectedItem("Select");
                    emailtxt.setText("");
                    salarytxt.setText("");
                    birthdaytxt.setDate(null);
                    addresstxt.setText("");
                    JOptionPane.showMessageDialog(null,"Employee Added Successfully.");
                }
                con.close();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"Add Employee Error: "+ e);
            }
        }
    }
